<h2 class="subhead">B.Tech (Computer Science and Engineering)</h2>
<p align="justify">The B.Tech in Computer Science and Engineering is one of the most popular programmes at N.I.T.K Surathkal. The programme was stated in 1986 and has been designed to cater to global demands and offers a balanced coverage of hardware and software systems. The programme includes elective courses covering thrust areas such as Distributed Systems & Computing, Parallel Architectures & High performance computing, High-Speed Networks, Designing with Network Processors, Security and Embedded Systems Cloud Computing etc. The B.Tech. programme enjoys an enviable placement of almost 100% year after year. About 25 to 35% of the students pursue higher studies after their graduation, mostly in U.S with scholarship and even without fellowship. </p>

<p>Average Intake (per year) :110</p>
<p>Admission : Through JEE(Main) as per guidelines of MHRD</p>
