<h2 class="subhead">M.Tech (Computer Science and Engineering)</h2>
<p align="justify">The M.Tech.(CSE) is a four semester course and was started in 1996. The course is aimed at providing a comprehensive overview of the recent developments in various frontier areas of Computer Science and Engineering including Computer Architecture, Software Systems, Parallel and Distributed Computing and Networking with Web Technologies. To provide a sound basis, sufficient amount of theoretical input is also given to the students. Another feature of the programme is that the students will have ample opportunity to carry out research & software development in the labs. </p>

<p>Average Intake (per year) :27</p>
<p>Admission : Through GATE as per guidelines of MHRD</p>
<p><a href="https://www.nitk.ac.in/Curriculum"> M.Tech Curriculum [2019]  </a></p>
<p><a href="https://www.nitk.ac.in/Academic_Calendars">Academic Calender: M.Tech.[2020]</a></p>

