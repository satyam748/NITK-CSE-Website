<h2 class="subhead">M.Tech. (Computer Science and Engineering - Information Security)</h2>
<p align="justify">M.Tech(CSE-IS) is a four semester course aimed to focus on the recent developments in the areas of Information Security. The course was offered for the first time in 2007. The curriculum for these P.G programmes has been carefully crafted to cater the need of those students who are admitted with their basic degree in computing. In other words, care is taken, not to repeat the same, good old courses of U.G in the P.G level.</p>

<p>Average Intake (per year) :27</p>
<p>Admission : Through GATE as per guidelines of MHRD</p>
